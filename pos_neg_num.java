// Check whether a number is positive, negative, or zero
public class pos_neg_num {
    public static void main(String[]args){
        int num = 1 ;
        if(num>0){
            System.out.println("Positive");
        }
        else if(num==0){
            System.out.println("Zero");
        }
        else{
            
            System.out.println("Negative");
        }
    }
}
